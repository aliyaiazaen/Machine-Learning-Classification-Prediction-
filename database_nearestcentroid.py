from sklearn.neighbors import NearestCentroid

# Database: Gerbang logika AND
# x = Data, y = Target
x = [[0,0], [0,5], [2,0], [2,2], [2,7], [3,3], [3,5], [3,8], [4,2], [4,6]]
y = [0,1,0,2,4,3,5,6,0,1]

# Training and Classify
clf = NearestCentroid()
clf.fit(x, y)

# Prediksi
print ("Logika AND Metode K. Nearest Neighbors (KNN)") 
print ("Logika Prediksi")
print ("0 0 =", clf.predict([[0,0]])) 
print ("0 5 =", clf.predict([[0,5]])) 
print ("2 0 =", clf.predict([[2,0]]))
print ("2 2 =", clf.predict([[2,2]]))
print ("2 7 =", clf.predict([[2,7]])) 
print ("3 3 =", clf.predict([[3,3]]))
print ("3 5 =", clf.predict([[3,5]])) 
print ("3 8 =", clf.predict([[3,8]])) 
print ("4 2 =", clf.predict([[4,2]])) 
print ("4 6 =", clf.predict([[4,6]]))